public class Livro {
    private String titulo, autor;

    public String getAutor() {
        return autor;
    }
    
    public String getTitulo() {
        return titulo;
    }
    
    public void setAutor(String nomeAutor) {
        autor = nomeAutor;
    }

    public void setTitulo(String tituloLivro) {
        titulo = tituloLivro;
    }
}
